class Operator4
{
	public static void main(String[] args)
	{
		System.out.println(1 + 2);   // addition 3
		System.out.println("a" + 2); // concatenate -> a2
		System.out.println("1" +2);  // 12
		System.out.println('A' + 2); // 67 asci of A=65
		System.out.println('A' + '2');// 115 asci of 2=50
		System.out.println('A' + "2");// A2
	}
}